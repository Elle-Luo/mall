<template>
  <div class="tabcontrol">
    <div v-for="(item, index) in titles" 
      class="tabcontrol__item" 
      :class="{active: index === currentIndex}"
      @click="itemClick(index)"
      :key="index">
      <span>{{ item }}</span>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      currentIndex: 0
    }
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index
      this.$emit('tabClick', index)
    }
  }
};
</script>
